    private void btnSiguienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSiguienteMouseClicked
        // TODO add your handling code here:
        
        int verificar = verificarCampos(this.txtUsuarioActual, this.txtContraseniaActual);
        this.lblNotificacionDatos.setVisible(false);
        
        int verificar2 = 0;
        
        if(verificar  == 0 ){
            String userAct = this.txtUsuarioActual.getText();
            String passAct = this.txtContraseniaActual.getText();
              
            try{
                Administrador miAdmin = new Administrador(userAct, passAct);
                ArrayList<Object[]> datos = miAdmin.ingresar();    
                Object[] columnas = datos.get(1);
                String d1 = Arrays.toString(columnas);
                verificar2 = 1;
            }
            catch(Exception ne)
            {
                //JOptionPane.showMessageDialog(this, "Se presento la excepcion:   "+ ne.getMessage() );
                this.txtUsuarioActual.setText("");
                this.txtContraseniaActual.setText("");
                userAct = "";
                passAct = "";
                this.lblNotificacionDatos.setVisible(true);
                this.txtUsuarioNuevo.setEnabled(false);
                this.txtContraseniaNueva.setEnabled(false);
                verificar2 = 2;
            }
            finally{
                
            }
            
            
            if(verificar2 == 1){
                this.txtUsuarioNuevo.setEnabled(true);
                this.txtContraseniaNueva.setEnabled(true);
                this.txtContraseniaActual.setEnabled(false);
                this.txtUsuarioActual.setEnabled(false);
            }
                
        }else{
            JOptionPane.showMessageDialog(this, "Falta datos!!");
            
        }
    }//GEN-LAST:event_btnSiguienteMouseClicked


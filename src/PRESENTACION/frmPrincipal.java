    private void btnActualizarDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnActualizarDatosMouseClicked
        // TODO add your handling code here:

        int verificar = verificarCampos(this.txtUsuarioNuevo, this.txtContraseniaNueva);
        
        if(verificar  == 0 ){
            String userNew = this.txtUsuarioNuevo.getText();
            String passNew = this.txtContraseniaNueva.getText();
            String userAct = this.txtUsuarioActual.getText();
              
            
            Administrador miAdmin = new Administrador(userNew, passNew);
            int res = miAdmin.actualizarDatos(userAct);
            
            if (res == 1) {
                JOptionPane.showMessageDialog(this, "Datos Actualizados");
                frmPrincipal myPrincipal = new frmPrincipal();
                myPrincipal.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Datos NO Actualizados");
            }
                
        }else{
            JOptionPane.showMessageDialog(this, "Falta datos!!");
        }  
    }//GEN-LAST:event_btnActualizarDatosMouseClicked


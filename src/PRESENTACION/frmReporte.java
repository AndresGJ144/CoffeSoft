                my_report_table.setWidthPercentage(100f);

                //create a cell object
                PdfPCell table_cell;

                while (query_set.next()) {
                    String Fecha = query_set.getString("EVEFECHA");
                    table_cell = new PdfPCell(new Phrase(Fecha));
                    my_report_table.addCell(table_cell);
                    String Nombre = query_set.getString("EVENOMBRE");
                    table_cell = new PdfPCell(new Phrase(Nombre));
                    my_report_table.addCell(table_cell);
                    String Nombre_Empleado = query_set.getString("EMPNOMBRE");
                    table_cell = new PdfPCell(new Phrase(Nombre_Empleado));
                    my_report_table.addCell(table_cell);
                    String Descripcion = query_set.getString("EVEDESCRIPCION");
                    table_cell = new PdfPCell(new Phrase(Descripcion));
                    my_report_table.addCell(table_cell);
                    String Costo = query_set.getString("EVECOSTO");
                    table_cell = new PdfPCell(new Phrase(Costo));
                    my_report_table.addCell(table_cell);
                }
                /* Attach report table to PDF */
                document.add(tablaLotes);
                document.add(my_report_table);
                document.close();

                /* Close all DB related objects */
                query_set.close();
                stmt.close();

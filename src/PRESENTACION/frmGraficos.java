    private void btnCircularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCircularActionPerformed
       
         
         DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        ///metodo de carga de informacion en la tabla
        EntityManagerFactory emfactory = Persistence.createEntityManagerFactory( "CafeSoft_V1.2PU" );
        EntityManager entitymanager = emfactory.createEntityManager( );
         //Scalar function
                      //Inicio modificable el resto es igual   
        Query query = entitymanager.createQuery("SELECT m FROM Materiales m");
         //Query query = entitymanager.createQuery("SELECT t FROM Tablaprueba t");
        
        List<Materiales> list=query.getResultList();
        if(!list.isEmpty()){
            for(int i =0;i<list.size();i++){
           ;
                dataset.setValue( list.get(i).getMatcosto(),list.get(i).getMatnombre(),"");
                        
            }
               
                }
        
        
        JFreeChart chart = ChartFactory.createBarChart3D("Grafico de costos[unidad] por mes","Insumos", "Costos [pesos]", 
        dataset, PlotOrientation.VERTICAL, true,true, false);
        chart.setBackgroundPaint(Color.cyan);
        chart.getTitle().setPaint(Color.black); 
        CategoryPlot p = chart.getCategoryPlot(); 
        p.setRangeGridlinePaint(Color.white); 
        // Mostrar Grafico
        ChartPanel chartPanel = new ChartPanel(chart);
        
        
        dispose();
      
        ChartFrame frame = new ChartFrame("Grafico de costos[unidad] por mes",chart);
        frame.setVisible(true);
        frame.setSize(1300, 700);
        
        
    }//GEN-LAST:event_btnCircularActionPerformed

